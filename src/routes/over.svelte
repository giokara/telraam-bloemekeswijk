    <p>De voorbije jaren waren er al verschillende pogingen om concrete actie te voeren rond de verkeersveiligheid op de buurt. Er waren
        gesprekken met de bevoegde schepen. We verzamelden kleine en haalbare ingrepen in een korte nota en bezorgden deze aan het stadsbestuur.
        Buren vergaderden, breinen stormden, op facebook schuurden onze meningen langs elkaar. Er vielen spaanders, maar er werd geen
        hout gehakt. Weinig echt resultaat dus, wat ertoe leidde dat bewoners afhaakten en pen en strijdbijl wegborgen.</p>
    <p>Toen het stadsbestuur aankondigde dat er wijkmobiliteitsplannen kwamen, ging het waaklampje weer flikkeren. Hier lag een kans om op
        lange termijn met zicht op concrete aanpassingen te  werken. In plaats van ons te verliezen in details (fietsrekken vs autoparking,
        verkeersdrempels vs asverschuiving, eenrichtingsverkeer of toch beter niet), kunnen we deze keer meedenken over de
        verkeerscirculatie in en door de wijk. En om te voorkomen dat we onszelf weerom klem rijden in detaildiscussies, kiezen we
        ervoor om data te verzamelen. Voor je iets wil oplossen, moet je weten wat het probleem is. En dat doe je door te meten.</p>
    <p>En zo kwamen we terecht bij <a href="https://telraam.net/nl/what-is-telraam" alt="wat is telraam"><Badge type="primary">Telraam</Badge></a>. Dit is een stukje betaalbare en eenvoudige technologie dat informatie verzamelt
        over de verkeersstroom op een bepaalde locatie. De sensor meet hoeveel auto's, fietsers en voetgangers passeren en aan
        welke snelheid (bij benadering) de auto's er rijden.</p>
    <p>Bedoeling is om op dertig meetpunten slim gespreid over zoveel mogelijk straten  minstens een jaar lang te registreren. Zo leren we
        in welke straten het druk/minder druk is en hoe dat zit met het respecteren van de snelheidslimiet. Dit is van goudwaarde bij het
        bespreken van het toekomstige wijkmobiliteitsplan. Dit zijn onweerlegbare feiten, en geen meningen of interpretaties. En dus
        ideaal fundament om aan een duurzame constructieve dialoog rond wijkmobiliteit te bouwen.</p>

